package egovframework.fusion.galary.vo;

public class DownloadsVO {
	
	private Integer download_id;
	private Integer media_id;
	private String download_date;
	private String user_id;
	
	public Integer getDownload_id() {
		return download_id;
	}
	public void setDownload_id(Integer download_id) {
		this.download_id = download_id;
	}
	public Integer getMedia_id() {
		return media_id;
	}
	public void setMedia_id(Integer media_id) {
		this.media_id = media_id;
	}
	public String getDownload_date() {
		return download_date;
	}
	public void setDownload_date(String download_date) {
		this.download_date = download_date;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	}
